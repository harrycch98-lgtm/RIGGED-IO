const fs = require('fs');
const path = require('path');

const root = __dirname;
const outputDirectory = path.join(root, 'crazygames-build');

function read(name) {
  return fs.readFileSync(path.join(root, name), 'utf8').replace(/^\uFEFF/, '');
}

function dataUri(name, mime) {
  const encoded = fs.readFileSync(path.join(root, name)).toString('base64');
  return `data:${mime};base64,${encoded}`;
}

const assets = {
  map: dataUri('us_world_map.png', 'image/png'),
  logo: dataUri('rigged-logo.png', 'image/png'),
  assassination: dataUri('assassinate_broadcast_v2.gif', 'image/gif'),
  powerGrab: dataUri('power_grab_broadcast.gif', 'image/gif'),
  menu: dataUri('bgm-main-menu.mp3', 'audio/mpeg'),
  early: dataUri('bgm-early-game.mp3', 'audio/mpeg'),
  mid1: dataUri('bgm-mid-game-1.mp3', 'audio/mpeg'),
  mid2: dataUri('bgm-mid-game-2.mp3', 'audio/mpeg'),
  end1: dataUri('bgm-end-game-1.mp3', 'audio/mpeg'),
  end2: dataUri('bgm-end-game-2.mp3', 'audio/mpeg'),
  victory: dataUri('bgm-victory.mp3', 'audio/mpeg'),
};

let css = read('game.css')
  // Use the bundled fallback fonts so the build has no Google Fonts dependency.
  .replace(/^@import url\([^\n]+\);\s*/i, '');

let game = read('game.js')
  .replace('"rigged-logo.png?v=2"', JSON.stringify(assets.logo))
  .replace('"us_world_map.png?v=5"', JSON.stringify(assets.map))
  .replace('"bgm-main-menu.mp3"', JSON.stringify(assets.menu))
  .replace('"bgm-early-game.mp3"', JSON.stringify(assets.early))
  .replace('"bgm-mid-game-1.mp3"', JSON.stringify(assets.mid1))
  .replace('"bgm-mid-game-2.mp3"', JSON.stringify(assets.mid2))
  .replace('"bgm-end-game-1.mp3"', JSON.stringify(assets.end1))
  .replace('"bgm-end-game-2.mp3"', JSON.stringify(assets.end2))
  .replace('"bgm-victory.mp3"', JSON.stringify(assets.victory))
  .replace('assassinationGif.src = `assassinate_broadcast_v2.gif?v=${Date.now()}`;', `assassinationGif.src = ${JSON.stringify(assets.assassination)};`)
  .replace('powerGrabGif.src = `power_grab_broadcast.gif?v=${Date.now()}`;', `powerGrabGif.src = ${JSON.stringify(assets.powerGrab)};`)
  .replace(/<\/script/gi, '<\\/script');

const multiplayer = read('multiplayer-server.js').replace(/<\/script/gi, '<\\/script');
let html = read('index.html');

  html = html
  .replace(/\s*<script async src="https:\/\/pagead2\.googlesyndication\.com[^>]+><\/script>/i, '')
  // Use replacement callbacks so JavaScript sequences such as "$&" remain literal.
  .replace(/\s*<link rel="stylesheet" href="game\.css\?v=\d+">/i, () => `\n  <style>\n${css}\n  </style>`)
  .replace(/src="rigged-logo\.png\?v=2"/g, () => `src="${assets.logo}"`)
  .replace('src="assassinate_broadcast_v2.gif"', () => `src="${assets.assassination}"`)
  .replace('src="power_grab_broadcast.gif"', () => `src="${assets.powerGrab}"`)
  .replace(/\s*<script src="multiplayer-server\.js\?v=\d+"><\/script>/i, () => `\n  <script>\n${multiplayer}\n  </script>`)
  .replace(/\s*<script src="game\.js\?v=\d+"><\/script>/i, () => `\n  <script>\n${game}\n  </script>`)
  .replace('<head>', '<head>\n  <!-- Self-contained CrazyGames build generated by build-crazygames-single.js -->');

fs.mkdirSync(outputDirectory, { recursive: true });
const output = path.join(outputDirectory, 'index.html');
fs.writeFileSync(output, html);
console.log(`${output}\n${fs.statSync(output).size} bytes`);
